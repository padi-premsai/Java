// This is the definition of the class named MyClass
public class MyClass {
    
    // This is the main method where the execution of the program starts
    public static void main(String[] args) {
        
        // This is a variable declaration and initialization
        int number = 10;
        
        // This is a print statement that outputs the value of the variable to the console
        System.out.println("The value of the number is: " + number);
        
        // This is a conditional statement that checks whether the number is greater than 5
        if (number > 5) {
            // This block of code is executed if the condition is true
            System.out.println("The number is greater than 5.");
        } else {
            // This block of code is executed if the condition is false
            System.out.println("The number is not greater than 5.");
        }
    }
}
